#Q5
import plotly.figure_factory as ff

# Create a distribution plot using Plotly Express (Distplot)
data = [titanic['age'].dropna()]  # Use age column from the Titanic dataset for demonstration
group_labels = ['Age Distribution']

# Create distplot
fig5 = ff.create_distplot(data, group_labels, show_hist=True, show_rug=False)
fig5.update_layout(title='Distplot of Age Distribution in Titanic Dataset')
fig5.show()

