import plotly.express as px

# Load the tips dataset using Plotly Express
tips = px.data.tips()

# Create a histogram using Plotly Express
fig3 = px.histogram(tips, x="sex", y="total_bill", color="day", pattern_shape="smoker", title='Histogram of Total Bill by Sex with Day and Smoker Pattern')
